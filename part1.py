import pandas as pd
import numpy as np

# Создание DataFrame с данными об оценках учеников
data = {
    'Ученик': ['Иван', 'Анна', 'Петр', 'Мария', 'Сергей', 'Елена', 'Алексей', 'Ольга', 'Никита', 'Дарья'],
    'Математика': [85, 92, 78, 88, 91, 73, 95, 87, 89, 82],
    'Русский язык': [78, 85, 80, 90, 84, 76, 88, 85, 89, 77],
    'Английский язык': [90, 88, 85, 92, 87, 80, 94, 89, 86, 84],
    'Физика': [72, 85, 80, 88, 83, 75, 89, 84, 86, 78],
    'Химия': [88, 90, 84, 86, 85, 80, 92, 87, 89, 82]
}

# Создание DataFrame
df = pd.DataFrame(data)

# Вывод первых строк DataFrame
print("Первые строки таблицы:\n", df.head())

# Средняя оценка по каждому предмету
mean_scores = df.mean(numeric_only=True)
print("\nСредние оценки по каждому предмету:\n", mean_scores)

# Медианная оценка по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианные оценки по каждому предмету:\n", median_scores)

# Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print("\nQ1 (25-й перцентиль) для математики:",Q1_math)
print("Q3 (75-й перцентиль) для математики:",Q3_math)
print("IQR (межквартильный размах) для математики:",IQR_math)

# Стандартное отклонение для каждого предмета
std_deviation = df.std(numeric_only=True)
print("\nСтандартное отклонение по каждому предмету:\n", std_deviation)
